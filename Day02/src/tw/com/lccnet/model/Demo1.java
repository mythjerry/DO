package tw.com.lccnet.model;

public class Demo1 {
		/**
		 * public 公開(程式的入口)
		 * static 靜態，當程式進行時屬於靜態設定就會先切割記憶體
		 * 就可以馬上使用，每一個專案都會有一個main方法
		 * void 無回傳值
		 * main 方法名稱固定(入門方法)
		 * String 字串型態，需要搭配雙引號" "包起來
		 * [ ]陣列
		 * args 為String[ ]的變數名稱
		 */

	public static void main(String[] args) {
		System.out.println("123");
	}

}
