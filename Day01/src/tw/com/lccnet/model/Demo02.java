package tw.com.lccnet.model;

/**
 *  	這個class主功能為示範用得類別
 *
 */

public class Demo02 {
	/**
	 * 這個java的主要方法，為程式的進入點
	 * 
	 */

	public static void main(String[] args) {
		//這是一個變數，int 為型態，a為變數名稱，100為變數值
		int a = 100;
		/*
		 * System代表系統
		 * out 輸出
		 * println(); 代表列印至下一行
		 * print();代表列印
		 * a為變數值
		 * ;結束這行程式 
		 * 快捷鍵 syso alt+/
		 */
		 System.out.println(a);
	}

}
